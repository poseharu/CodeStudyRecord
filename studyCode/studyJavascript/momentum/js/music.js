function initVolume(volume){
  const musicVolume = document.querySelector("#musicVolume i");
  const volumeSetting = document.querySelector("#volumeSetting");

  musicAudio.volume = volumeSetting.value = volume;
  if(volume === "0") musicVolume.classList.replace("fa-volume-high", "fa-volume-xmark");
  else musicVolume.classList.replace("fa-volume-xmark", "fa-volume-high");
}

function musicVolume(){
  const musicVolume = document.querySelector("#musicVolume i");

  if(musicVolume.classList.contains("fa-volume-high")) initVolume("0");
  else initVolume(currentMusicMod.isVulume);
}

