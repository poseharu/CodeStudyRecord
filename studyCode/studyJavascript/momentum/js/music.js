  const durationTimeEl = document.querySelector("#durationTime");
  const musicProgressEl = document.querySelector("#musicProgress");
  const progressPercent = (musicAudio.currentTime / musicAudio.duration) * 100;
  musicProgressEl.style.width = `${progressPercent}%`;
    
