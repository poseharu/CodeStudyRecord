const MUSIC_KEY = "musicSrtting";
const musicAudio = new Audio();
const currentMusicMod = {
  index: 0,
  isPlay: false,
  isOnce: false,
  isRepeat: false,
  isShuffle: false,
  isVulume:1,
  historyMusicList:[],
  prevIndex: function(){
    if(this.index === 0) this.index = musicList.length-1;
    else this.index -= 1;
    return this.index;
  },
  nextIndex: function(){
    if(this.index === musicList.length-1) this.index = 0;
    else this.index += 1;
    return this.index;
  },
  randomIndex: function(){
    let randomNum = this.index;
    while(randomNum === this.index){
      randomNum = Math.floor(Math.random() * musicList.length);
    }
    return this.index = randomNum;
  }
}

