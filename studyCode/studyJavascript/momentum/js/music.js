//현재 재생중인 음악이 종료 되었을때
function musicPlayingEnd(){
  //여러번 반복o 서플o 일때는 랜덤하게 반복하면됨
  //여러번 반복o 서플x 일때는 해당 위치에서 재생 시작하고, 순서대로 플레이 리스트 반복하면됨
  //여러번 반복x 서플o 일때는 랜덤하게 곡을 재생하고, 이미 재생된 곡을 만나면 멈춤
  //여러번 반복x 서플x 일때는 해당 위치에서 끝까지만 재생하면됨 (곡 수만큼은 아님 ex 3번~5번까지 3곡 재생)
  //한번 반복o 서플o 일때는 한곡만 반복 재생
  //한번 반복o 서플x 일때는 한곡만 반복 재생
  //한번 반복x 서플o 일때는 한곡만 재생하고 종료
  //한번 반복x 서플x 일때는 한곡만 재생하고 종료

  //한곡만
  if(currentMusicMod.isOnce){
    //반복o
    if(currentMusicMod.isRepeat){
      musicAudio.play();
    }
    //반복X
    else{
      currentMusicMod.isPlay = false;
      musicPlayAndPauseIcon();
    }
  }
  //여러곡
  else{
    //반복o
    if(currentMusicMod.isRepeat){
      //서플o
      if(currentMusicMod.isShuffle){
        musicAudio.src = musicList[currentMusicMod.randomIndex()].fileUrl;
        musicAudio.play();
      }
      //서플X
      else{
        musicAudio.src = musicList[currentMusicMod.nextIndex()].fileUrl;
        musicAudio.play();
      }
    }
    //반복X
    else{
      //서플o
      if(currentMusicMod.isShuffle){
        const shuffleNumber = currentMusicMod.randomIndex();
        if(currentMusicMod.historyMusicList.some((value)=> value === shuffleNumber)){
          currentMusicMod.isPlay = false;
          musicPlayAndPauseIcon();
        }
        else{
          currentMusicMod.historyMusicList.push(currentMusicMod.index);
          musicAudio.src = musicList[currentMusicMod.index].fileUrl;
          musicAudio.play();
        }
      }
      //서플X
      else{
        if(currentMusicMod.index === musicList.length-1) {
          currentMusicMod.isPlay = false;
          musicPlayAndPauseIcon();
        }
        else{
          musicAudio.src = musicList[currentMusicMod.nextIndex()].fileUrl;
          musicAudio.play();
        }
      }
    }
  }
  if(currentMusicMod.isPlay){
    makeMusicInfo();
    //설정값 저장
    saveMusicSetting();
  }
}

